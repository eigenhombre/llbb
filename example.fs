\\ "Forth" calculator example

3   \\ push 3 on stack
66  \\ push 66 on stack
.   \\ print item on top of stack; prints "66"
*   \\ removes 66 and 3 and replaces top of stack with 198
77  \\ push 77 on stack
.   \\ prints "77"
*   \\ removes 77 and 198 and replaces top of stack with 77 * 198 = 15246
.   \\ prints "15246"

